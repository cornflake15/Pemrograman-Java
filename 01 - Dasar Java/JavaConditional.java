public class JavaConditional {
	public static void main(String [] args){
		// Switch Case
		int nomorbulan = 4;

		switch(nomorbulan){
			case 1:
				System.out.println("Januari");
				break;
			case 2:
				System.out.println("Februari");
				break;
			case 3:
				System.out.println("Maret");
				break;
			default:
				System.out.println("Nomor bulan tidak dikenali.");
				break;
		}

	}
}
